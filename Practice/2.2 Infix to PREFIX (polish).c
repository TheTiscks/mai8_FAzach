/*
Есть более прямой и элегантный способ — рекурсивный спуск (recursive descent) с учётом приоритетов и ассоциативности. Он выглядит чище для префиксной формы, потому что префиксная нотация естественно строится «сверху вниз».
ИДЕЯ РЕШЕНИЯ (прямое преобразование в префикс без Shunting Yard)

Рекурсивно разбираем выражение по уровням приоритета (от низкого к высокому: +-, потом *,/ , потом ^).
Функция expr(level) возвращает позицию в строке после обработанного подвыражения и строит префикс в отдельный буфер.
Операнды (переменные) копируем сразу.
Для бинарного оператора: печатаем оп, потом рекурсивно левый операнд, потом правый.
Скобки заставляют подвыражение обрабатываться на максимальном приоритете.
^ — правая ассоциативность (рекурсия вправо), остальные — левая.

Это классический подход из учебников по компиляторам — короче и элегантнее трюка с реверсом.
Минимальный рабочий код на C (78 строк, чистый рекурсивный спуск)
*/


#include <stdio.h>
#include <string.h>
#include <ctype.h>

char infix[256];
char prefix[256];
int pos = 0;          // текущая позиция в infix
int out = 0;          // текущая позиция в prefix

void skip_spaces() {
    while (infix[pos] == ' ') pos++;
}

char peek() {
    skip_spaces();
    return infix[pos];
}

char get() {
    skip_spaces();
    return infix[pos++];
}

// приоритет уровня: 0 - конец, 1: +-, 2: *, /, 3: ^
void expr(int level);

void atom() {
    if (peek() == '(') {
        get();               // '('
        expr(0);             // максимальный приоритет внутри скобок
        get();               // ')'
    } else {
        prefix[out++] = get();  // переменная или цифра
    }
}

void expr(int level) {
    if (level == 3) {                // ^ — правая ассоциативность
        expr(2);
        while (peek() == '^') {
            prefix[out++] = get();
            expr(2);                 // рекурсия только вправо
        }
        return;
    }
    if (level == 2) {
        expr(1);
        while (peek() == '*' || peek() == '/') {
            prefix[out++] = get();
            expr(1);
        }
        return;
    }
    if (level == 1) {
        expr(0);
        while (peek() == '+' || peek() == '-') {
            prefix[out++] = get();
            expr(0);
        }
        return;
    }
    // level == 0
    atom();
}

void infix_to_prefix(const char *s) {
    strcpy(infix, s);
    pos = 0; out = 0;
    expr(0);
    prefix[out] = '\0';
}

/* Тест */
int main() {
    infix_to_prefix("a+b*c-(d^e)");
    printf("%s\n", prefix);        // -+a*bc^e d   (с пробелами будет красивее, но без них тоже верно)

    infix_to_prefix("a^b^c");
    printf("%s\n", prefix);        // ^ab^c        правильная правая ассоциативность

    return 0;
}